    public static final int SUBSYSTEM1_JAG2 = -1;
    public static final int SUBSYSTEM2_JAG1 = -1;
    public static final int SUBSYSTEM2_JAG2 = -1;
    public static final int SUBSYSTEM1_SERVO1 = -1;
    public static final int SUBSYSTEM1_SERVO2 = -1;
    public static final int SUBSYSTEM2_SERVO1 = -1;
    public static final int SUBSYSTEM2_SERVO2 = -1;
    
    // Joysticks
    public static final int JOYSTICK_PORT = 1;
    public static final int JOYSTICK2_PORT = 2;
    
    // Analog
    public static final int ACCELEROMETER_PORT = -1;
    public static final int GYRO_PORT = -1;
